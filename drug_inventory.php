<script>
    function openAddSupplyModal() {
        document.getElementById('modalTitle').innerText = 'Add Supply';
        document.getElementById('submitButton').innerText = 'Add Supply';
        document.getElementById('submitButton').name = 'add_supply';
        clearForm();
        document.getElementById('supplyModal').style.display = 'block';
    }

    function openEditSupplyModal(supply) {
    document.getElementById('modalTitle').innerText = 'Edit Supply';
    document.getElementById('submitButton').innerText = 'Update Supply';
    document.getElementById('submitButton').name = 'edit_supply';
    document.getElementById('supplyId').value = supply.SupplyID; // Hidden field
    populateForm(supply);
    document.getElementById('supplyModal').style.display = 'block';
}


    function closeModal() {
        document.getElementById('supplyModal').style.display = 'none';
    }

    function clearForm() {
        document.getElementById('drugHeaderId').value = '';
        document.getElementById('vendorId').value = '';
        document.getElementById('quantity').value = '';
        document.getElementById('manufactureDate').value = '';
        document.getElementById('expiryDate').value = '';
    }

    function populateForm(supply) {
        document.getElementById('drugHeaderId').value = supply.DrugHeaderID; // Use DrugHeaderID
        document.getElementById('vendorId').value = supply.VendorID;         // Use VendorID
        document.getElementById('quantity').value = supply.Quantity;
        document.getElementById('manufactureDate').value = supply.Manufacture_Date;
        document.getElementById('expiryDate').value = supply.ExpiryDate;
    }
</script>

