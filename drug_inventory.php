// Handle form submissions
if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    if (isset($_POST['add_supply']) || isset($_POST['edit_supply'])) {
        $drugHeaderID = $_POST['drug_header_id'];
        $vendorID = $_POST['vendor_id'];
        $quantity = $_POST['quantity'];
        $manufactureDate = $_POST['manufacture_date'];
        $expiryDate = $_POST['expiry_date'];

        if (isset($_POST['edit_supply'])) {
            $supplyID = $_POST['supply_id'];

            // Update existing supply
            $stmt = $conn->prepare("UPDATE drug_supply SET DrugHeaderID = ?, VendorID = ?, Quantity = ?, Manufacture_Date = ?, ExpiryDate = ? WHERE SupplyID = ?");
            $stmt->bind_param("ssisss", $drugHeaderID, $vendorID, $quantity, $manufactureDate, $expiryDate, $supplyID);
        } else {
            // Insert new supply (SupplyID will be auto-generated by the trigger)
            $stmt = $conn->prepare("INSERT INTO drug_supply (DrugHeaderID, VendorID, Quantity, Manufacture_Date, ExpiryDate) VALUES (?, ?, ?, ?, ?)");
            $stmt->bind_param("ssiss", $drugHeaderID, $vendorID, $quantity, $manufactureDate, $expiryDate);
        }

        if ($stmt->execute()) {
            echo "<script>alert('Supply successfully " . (isset($_POST['edit_supply']) ? "updated" : "added") . "!');
                window.location.href = 'drug_inventory.php';
            </script>";
        } else {
            echo "<script>alert('Error: " . $conn->error . "');</script>";
        }
        $stmt->close();
    }

    // Handle delete supply request
    if (isset($_POST['delete_supply'])) {
        $supplyID = $_POST['supply_id'];

        $stmt = $conn->prepare("DELETE FROM drug_supply WHERE SupplyID = ?");
        $stmt->bind_param("s", $supplyID);

        if ($stmt->execute()) {
            echo "<script>alert('Supply successfully deleted!');
                window.location.href = 'drug_inventory.php';
            </script>";
        } else {
            echo "<script>alert('Error: " . $conn->error . "');</script>";
        }
        $stmt->close();
    }
}

